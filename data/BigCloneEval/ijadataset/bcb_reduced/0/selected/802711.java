    private void splitterSplitButtonActionPerformed(java.awt.event.ActionEvent evt) {
        if (splitterCheckBox.isSelected()) {
            deleteFileAfterSplit = true;
            status.setText("Rozdělovač: Poznámka- Původní soubor bude po rozdělení smazán..");
        }
        try {
            if (partsRadioButton.isSelected()) {
                setSplitSize();
                if (noofparts < 2) {
                    JOptionPane.showMessageDialog(this, "Počet částí musí být alespoň 2!", "Oops", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                new SplitterByParts(splitterInputFile, splitterOutputFile, noofparts, splitterProgressBar, status, deleteFileAfterSplit);
            } else {
                if (splitsize > filesize) {
                    JOptionPane.showMessageDialog(this, "Velikosti jednotlivých částí rozděleného souboru musí být menší než původní velikost souboru! *uff..*", "Oops", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                setNoOfParts();
                new SplitterBySize(splitterInputFile, splitterOutputFile, splitsize, splitterProgressBar, status, deleteFileAfterSplit);
            }
        } catch (Exception ex) {
        }
    }

