    private void textFieldValidator(java.awt.event.KeyEvent evt) {
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c) || (c == java.awt.event.KeyEvent.VK_BACK_SPACE) || (c == java.awt.event.KeyEvent.VK_DELETE))) {
            getToolkit().beep();
            evt.consume();
        }
        if (splitterInputFile == null) {
            JOptionPane.showMessageDialog(this, "Vyberte nejprve vstupní soubor!", "Oops", JOptionPane.WARNING_MESSAGE);
            evt.consume();
        }
    }

    private void setSplitSize() {
        if (!noofpartsTextField.getText().equals("")) noofparts = Integer.parseInt(noofpartsTextField.getText());
        long filesize = splitterInputFile.length() / noofparts;
        if (filesize >= 1048576) {
            splitSizeComboBox.setSelectedItem("MB");
            splitSizeTextField.setText((filesize / 1048576) + "");
        } else if (filesize >= 1024) {
            splitSizeComboBox.setSelectedItem("KB");
            splitSizeTextField.setText((filesize / 1024) + "");
        } else {
            splitSizeComboBox.setSelectedItem("bajtů");
            splitSizeTextField.setText(filesize + "");
        }
    }

    private void setNoOfParts() {
        if (splitSizeTextField.getText().equals("")) return;
        if (splitSizeComboBox.getSelectedItem().equals("MB")) splitsize = Integer.parseInt(splitSizeTextField.getText()) * 1048576; else if (splitSizeComboBox.getSelectedItem().equals("KB")) splitsize = Integer.parseInt(splitSizeTextField.getText()) * 1024; else splitsize = Integer.parseInt(splitSizeTextField.getText());
        noofparts = (int) ((splitterInputFile.length() % splitsize == 0) ? (splitterInputFile.length() / splitsize) : (splitterInputFile.length() / splitsize) + 1);
        noofpartsTextField.setText(noofparts + "");
    }

