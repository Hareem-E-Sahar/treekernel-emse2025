    private void joinerSelectFileButtonActionPerformed(java.awt.event.ActionEvent evt) {
        JFileChooser f = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("soubory JJSplit (.v1, .001)", "v1", "001");
        f.setFileFilter(filter);
        f.setAcceptAllFileFilterUsed(false);
        f.showOpenDialog(this);
        if (f.getSelectedFile() == null) return;
        joinerOutputFile = joinerInputFile = f.getSelectedFile();
        joinerInputFileTextField.setText(joinerInputFile + "");
        if ((joinerInputFile + "").endsWith(".v1")) joinerOutputFileName = joinerInputFile.getName().substring(0, joinerInputFile.getName().length() - 3); else joinerOutputFileName = joinerInputFile.getName().substring(0, joinerInputFile.getName().length() - 4);
        joinerOutputFileTextField.setText(joinerInputFile.getParent() + File.separator + joinerOutputFileName);
        joinerProgressBar.setValue(0);
        status.setText("Slučovač: První část vybrána..");
    }

    private void joinerSelectOutputDirButtonActionPerformed(java.awt.event.ActionEvent evt) {
        if (joinerInputFile == null) {
            JOptionPane.showMessageDialog(this, "Vyberte nejprve vstupní soubor!", "Oops", JOptionPane.WARNING_MESSAGE);
            status.setText("Slučovač: Vyberte nejprve vstupní soubor!");
            return;
        }
        JFileChooser f = new JFileChooser();
        f.setApproveButtonText("Vybrat umístění");
        f.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        f.showOpenDialog(this);
        if (f.getSelectedFile() != null) {
            joinerOutputFileTextField.setText(f.getSelectedFile() + File.separator + joinerOutputFileName);
            joinerOutputFile = new File(f.getSelectedFile() + File.separator + joinerInputFile.getName());
            status.setText("Slučovač: Výstupní adresář pro umístění sloučeného souboru byl změněn..");
        }
    }

    private void joinerJoinButtonActionPerformed(java.awt.event.ActionEvent evt) {
        if (new File(joinerOutputFileTextField.getText()).exists()) {
            JOptionPane.showMessageDialog(this, "Výstupní soubor již existuje!", "Oops", JOptionPane.ERROR_MESSAGE);
            status.setText("Slučovač: Výstupní soubor již existuje, zkuste změnit výstupní adresář..");
            return;
        }
        if (joinerCheckBox.isSelected()) {
            deleteFilesAfterJoin = true;
            status.setText("Slučovač: Poznámka- Části budou smazány jedna po druhé během slučování..");
        }
        if ((joinerInputFile + "").endsWith(".v1")) new Joiner(joinerInputFile, joinerOutputFile, joinerProgressBar, status, deleteFilesAfterJoin); else new JPfmJoiner(joinerInputFile, joinerOutputFile, joinerProgressBar, status, deleteFilesAfterJoin);
    }

