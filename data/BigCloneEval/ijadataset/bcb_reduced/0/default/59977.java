    private void find_buttonActionPerformed(java.awt.event.ActionEvent evt) {
        String proper = search_text.getText().trim();
        if (source_select.getSelectedItem().toString().equals("Disks")) {
            Vector primary = all.results;
            Vector secondary = all.cut_results;
            if (type_box.getSelectedItem().toString().equals("Only Files")) {
                find temp = new find();
                primary = temp.files_only(all.results);
                secondary = temp.cut_path(primary);
            } else if (type_box.getSelectedItem().toString().equals("Only Dirs")) {
                find temp = new find();
                primary = temp.dirs_only(all.results);
                secondary = temp.cut_path(primary);
            }
            DefaultTableModel tableModel = new DefaultTableModel();
            DefaultTableModel tableModel_b = new DefaultTableModel();
            DefaultTableModel tableModel_No = new DefaultTableModel();
            number_vec n = new number_vec();
            search_vec search = new search_vec();
            int all_vecs[] = { search.search(secondary, proper).size() };
            tableModel_No.addColumn("No", n.num_vec(n.bigger(all_vecs)));
            tableModel.addColumn("Search Results", search.search(secondary, proper));
            No_table.setModel(tableModel_No);
            table.setModel(tableModel);
            No_Scroll.getVerticalScrollBar().setModel(table_Scroll.getVerticalScrollBar().getModel());
            tableModel_b.addColumn("Search Results", search.search(primary, proper));
            table_b.setModel(tableModel_b);
        } else if (source_select.getSelectedItem().toString().equals("Google")) {
            web_search search = new web_search();
            try {
                search.google_search(proper);
            } catch (URISyntaxException ex) {
                Logger.getLogger(window.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (source_select.getSelectedItem().toString().equals("Isohunt")) {
            web_search search = new web_search();
            try {
                search.isohunt_search(proper);
            } catch (URISyntaxException ex) {
                Logger.getLogger(window.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (source_select.getSelectedItem().toString().equals("Youtube")) {
            web_search search = new web_search();
            try {
                search.youtube_search(proper);
            } catch (URISyntaxException ex) {
                Logger.getLogger(window.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {
        count++;
        if (count % 2 == 0) {
            int column = table.getSelectedColumn();
            int row = table.getSelectedRow();
            Object value = table_b.getValueAt(row, column);
            String file_name = (String) value;
            file_name = file_name.toLowerCase();
            File f = new File(file_name);
            try {
                Desktop desktop = null;
                if (Desktop.isDesktopSupported()) {
                    desktop = Desktop.getDesktop();
                }
                desktop.open(f);
            } catch (IOException ioe) {
                ioe.printStackTrace();
            }
        }
    }

    private void sortActionPerformed(java.awt.event.ActionEvent evt) {
        String proper = search_text.getText().trim();
        DefaultTableModel tableModel1 = new DefaultTableModel();
        DefaultTableModel tableModel_No = new DefaultTableModel();
        DefaultTableModel tableModel_b1 = new DefaultTableModel();
        seperator s1 = new seperator();
        number_vec n1 = new number_vec();
        search_vec search = new search_vec();
        int all_vecs[] = { s1.movies(search.search(all.cut_results, proper)).size(), s1.music(search.search(all.cut_results, proper)).size(), s1.documents(search.search(all.cut_results, proper)).size(), s1.images(search.search(all.cut_results, proper)).size() };
        tableModel_No.addColumn("No", n1.num_vec(n1.bigger(all_vecs)));
        if (all_vecs[0] != 0) {
            tableModel1.addColumn("Movies", s1.movies(search.search(all.cut_results, proper)));
        }
        if (all_vecs[1] != 0) {
            tableModel1.addColumn("Music", s1.music(search.search(all.cut_results, proper)));
        }
        if (all_vecs[2] != 0) {
            tableModel1.addColumn("Documents", s1.documents(search.search(all.cut_results, proper)));
        }
        if (all_vecs[3] != 0) {
            tableModel1.addColumn("Images", s1.images(search.search(all.cut_results, proper)));
        }
        table.setModel(tableModel1);
        No_table.setModel(tableModel_No);
        No_Scroll.getVerticalScrollBar().setModel(table_Scroll.getVerticalScrollBar().getModel());
        if (all_vecs[0] != 0) {
            tableModel_b1.addColumn("Movies", s1.movies(search.search(all.results, proper)));
        }
        if (all_vecs[1] != 0) {
            tableModel_b1.addColumn("Music", s1.music(search.search(all.results, proper)));
        }
        if (all_vecs[2] != 0) {
            tableModel_b1.addColumn("Documents", s1.documents(search.search(all.results, proper)));
        }
        if (all_vecs[3] != 0) {
            tableModel_b1.addColumn("Images", s1.images(search.search(all.results, proper)));
        }
        table_b.setModel(tableModel_b1);
    }

