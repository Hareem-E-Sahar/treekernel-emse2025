    private void agroupActionPerformed(java.awt.event.ActionEvent evt) {
        int i;
        int column = table.getSelectedColumn();
        int row = table.getSelectedRow();
        Object value = table.getValueAt(row, column);
        String file_name = (String) value;
        for (i = 0; i < groups.size(); i++) {
            Group new_group2 = new Group(group_box.getSelectedItem().toString());
            if (groups.get(i).equals(new_group2)) {
                new_group2.add_item(file_name);
                groups.set(i, new_group2);
            }
            DefaultTableModel tableModel_g = new DefaultTableModel();
            tableModel_g.addColumn("Contents", new_group2.items);
            Group_table.setModel(tableModel_g);
        }
    }

